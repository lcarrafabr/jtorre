    public void getFieldCredenciado(int codigo_click_credenciado) throws ParseException{
        //Buscar contato
        Credenciadas oCredenciadas = new Credenciadas();
        ResultSet rs = null;
        oCredenciadas.setCodigo_credenciado(codigo_click_credenciado);

        try {
            rs = oCredenciadas.getConsultar(conecta);
            
            rs.next();

//            Pegar campos
            ComboItem sup_Reg_OP = new ComboItem(rs.getInt("CODIGO_SUP_REG_OP"), rs.getString("PESSOA_CAMPO"));
            supRegOpjComboBox.setSelectedItem(sup_Reg_OP);
            
            ComboItem gerente = new ComboItem(rs.getInt("CODIGO_GERENTE"), rs.getString("PESSOA_CAMPO"));
            gerentejComboBox.setSelectedItem(gerente);
            
            ComboItem sup_op = new ComboItem(rs.getInt("CODIGO_SUP_OP"), rs.getString("PESSOA_CAMPO"));
            supOperacoesjComboBox.setSelectedItem(sup_op);
            
            ComboItem sup_vendas = new ComboItem(rs.getInt("CODIGO_SUP_VENDAS"), rs.getString("PESSOA_CAMPO"));
            supVendasjComboBox.setSelectedItem(sup_vendas);
            
            ComboItem monitor = new ComboItem(rs.getInt("CODIGO_MONITOR"), rs.getString("PESSOA_CAMPO"));
            monitorjComboBox.setSelectedItem(monitor);
            
            ComboItem consultor = new ComboItem(rs.getInt("CODIGO_CONSULTOR"), rs.getString("PESSOA_CAMPO"));
            consultorjComboBox.setSelectedItem(consultor);
            
            pdvjTextField.setText(rs.getString("PDV"));
            codigoCredenciadajTextField.setText(rs.getString("NUMERO_CODIGO"));
            ufjComboBox.setSelectedItem(rs.getString("UF"));
            microRegiaojTextField.setText(rs.getString("MICRO_REGIAO"));
            cnpjjFormattedTextField.setText(rs.getString("CNPJ"));
            
            
        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
    
