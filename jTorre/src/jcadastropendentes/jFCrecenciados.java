    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Credenciadas oCredenciadas = new Credenciadas();
        
        //verifica hierarquia
        if(supRegOpjComboBox.getSelectedItem().equals("SELECIONE") || gerentejComboBox.getSelectedItem().equals("SELECIONE") ||
           supOperacoesjComboBox.getSelectedItem().equals("SELECIONE") || supVendasjComboBox.getSelectedItem().equals("SELECIONE") ||
           monitorjComboBox.getSelectedItem().equals("SELECIONE") || consultorjComboBox.getSelectedItem().equals("SELECIONE")){
         
            JOptionPane.showMessageDialog(null, "Preencha todos os campos do formulário hierarquia", "mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else{
            
            //verifica dados da credenciada
            if(pdvjTextField.getText().equals("") || codigoCredenciadajTextField.getText().equals("")){
                
             JOptionPane.showMessageDialog(null, "Preencha ao menos o \"PDV\" e \"Código\"\n no formulário DADOS DA CREDENCIADA.", "mensagem do sistema", JOptionPane.WARNING_MESSAGE);   
            }else{
                
                try {
                    salvarjButton.setEnabled(false);
                    novojButton.setEnabled(true);
                    editarjButton.setEnabled(true);
                    excluirjButton.setEnabled(false);
                    
                    combo_sup_reg_op = (ComboItem)supRegOpjComboBox.getSelectedItem();
                    combo_gerente    = (ComboItem)gerentejComboBox.getSelectedItem();
                    combo_superv_operacoes = (ComboItem)supOperacoesjComboBox.getSelectedItem();
                    combo_superv_vendas = (ComboItem)supVendasjComboBox.getSelectedItem();
                    combo_monitor_campo = (ComboItem)monitorjComboBox.getSelectedItem();
                    combo_consultor = (ComboItem) consultorjComboBox.getSelectedItem();
                    
                    oCredenciadas.setCodigo_pessoa_campo(recebe_codigo_pessoa);
                    oCredenciadas.setCodigo_sup_reg_op(combo_sup_reg_op.getId());
                    oCredenciadas.setCodigo_gerente(combo_gerente.getId());
                    oCredenciadas.setCodigo_sup_op(combo_superv_operacoes.getId());
                    oCredenciadas.setCodigo_sup_vendas(combo_superv_vendas.getId());
                    oCredenciadas.setCodigo_consultor(combo_consultor.getId());
                    oCredenciadas.setCodigo_monitor(combo_monitor_campo.getId());
                    
                    oCredenciadas.setPdv(pdvjTextField.getText().trim().toUpperCase());
                    oCredenciadas.setNumero_codigo(Integer.parseInt(codigoCredenciadajTextField.getText().trim()));
                    oCredenciadas.setUf(ufjComboBox.getSelectedItem().toString());
                    oCredenciadas.setMicro_regiao(microRegiaojTextField.getText().trim().toUpperCase());
                    oCredenciadas.setCnpj(cnpjjFormattedTextField.getText().toString());
                    oCredenciadas.setCadastrar();                 
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao cadastrar.\n"+e, "mensagem do sistema", JOptionPane.WARNING_MESSAGE); 
                }
                try {
                    getGradeCredenciado();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar grade.\n"+e, "mensagem do sistema", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

