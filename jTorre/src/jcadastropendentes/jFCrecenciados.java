        gradeCredenciadajTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeCredenciadajTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        gradeCredenciadajTable.getColumnModel().getColumn(2).setPreferredWidth(100);
        gradeCredenciadajTable.getColumnModel().getColumn(2).setPreferredWidth(100);

