    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Usuarios oUsuarios = new Usuarios();
        if (nivelUsuariojComboBox.getSelectedItem().equals("SELECIONE")){
          
            JOptionPane.showMessageDialog(null, "Escolha um nível de usuário", "Mensagem do sistema", JOptionPane.INFORMATION_MESSAGE);
            nivelUsuariojComboBox.grabFocus();
        }else{
            try {
                salvarjButton.setEnabled(false);
                novojButton.setEnabled(true);
                editarjButton.setEnabled(true);
                excluirjButton.setEnabled(false);
                
                oUsuarios.setCodigo_pessoa_usuario(recebe_codigo_pessoa);
                
                oUsuarios.setLogin(loginjTextField.getText().trim().toUpperCase());
                oUsuarios.setNivel_usuario(nivelUsuariojComboBox.getSelectedItem().toString());
                oUsuarios.setObservacao(observacaojTextArea.getText());
               oUsuarios.setCadastrar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar!\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
            }
            
            try {
            getGradeUsuarios();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar grade!\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
            
            
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

