     public void getGradeUsuarios() throws ParseException{
       //Consultar Pessoas
        Usuarios oUsuarios = new Usuarios();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "Usuário", "Login", "Nível"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeusuariojTable.setModel(modeloTabela);
        gradeusuariojTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeusuariojTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        gradeusuariojTable.getColumnModel().getColumn(2).setPreferredWidth(50);
        gradeusuariojTable.getColumnModel().getColumn(3).setPreferredWidth(50);

try {
            oUsuarios.setCodigo_pessoa_usuario(recebe_codigo_pessoa);
            resultSet = oUsuarios.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_PESSOA_USUARIO")),
                    resultSet.getString("NOME_PESSOA"),
                    resultSet.getString("LOGIN"),
                    resultSet.getString("NIVEL_USUARIO")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
     
     
     public void getFieldUsuario(int codigo_click_usuario) throws ParseException{
        //Buscar contato
        Usuarios oUsuarios = new Usuarios();
        ResultSet rs = null;
        oUsuarios.setCodigo_pessoa_usuario(codigo_click_usuario);

        try {
            rs = oUsuarios.getConsultar(conecta);
            rs.next();

            //Pegar campos
            loginjTextField.setText(rs.getString("LOGIN"));
            nivelUsuariojComboBox.setSelectedItem(rs.getString("NIVEL_USUARIO"));
            observacaojTextArea.setText(rs.getString("OBSERVACAO"));
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
