    public void getGradeSupervisorOperacoes() throws ParseException{
       //Consultar Pessoas
        PessoasCampo oPessoaCampo = new PessoasCampo();
        
        if ( supOpjTextField.getText().length() > 0 ){
            oPessoaCampo.setPessoa_campo(supOpjTextField.getText() +"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "Pessoa"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeSupOpjTable.setModel(modeloTabela);
        gradeSupOpjTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeSupOpjTable.getColumnModel().getColumn(1).setPreferredWidth(500);

try {
            oPessoaCampo.setCodigo_regional(recebe_codigo_regional);
            resultSet = oPessoaCampo.getConsultarSupervOperacoes(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(PessoasCampo.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_PESSOA_CAMPO")),
                    resultSet.getString("PESSOA_CAMPO")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(PessoasCampo.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    
