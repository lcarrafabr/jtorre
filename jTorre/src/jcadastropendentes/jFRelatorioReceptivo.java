   public void getGradeTelatorioReceptivo() throws ParseException{
       //Consultar Pessoas
        ControleLigacoesReceptivo oControleLigacoesReceptivo = new ControleLigacoesReceptivo();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Nome Agente"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeTotalLigacoesjTable.setModel(modeloTabela);
        gradeTotalLigacoesjTable.getColumnModel().getColumn(0).setPreferredWidth(150);

try {
            oControleLigacoesReceptivo.setData_cadastro(dataCadastrojFormattedTextField.getText().toString());
            oControleLigacoesReceptivo.setCodigo_pessoa(codido_pessoa);
            resultSet = oControleLigacoesReceptivo.getConsultar2(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    resultSet.getString("NOME_PESSOA"),

                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Telefones.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
