    private void visualizaLigacoesjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizaLigacoesjButtonActionPerformed
        // TODO add your handling code here:
        try {
            String data2 = dataCadastrojFormattedTextField.getText().toString();
            System.out.println(data2.toString());

            String caminhoRelJasper = "/Relatorios/RReceptivo.jasper";
            
                Map parametros = new HashMap();
                parametros.put("DATA2", data2);
                System.out.println("PARAMETRO: "+parametros.put("DATA_PESQUISA",data2.toString()));
                URL arquivo = getClass().getResource(caminhoRelJasper);
                if (arquivo == null) {
                    JOptionPane.showMessageDialog(null, "Arquivo jasper não encontrado");
                }
                JasperReport report;
                report = JasperManager.loadReport(arquivo.openStream());
                JasperPrint jasperPrint = JasperFillManager.fillReport(report, parametros, ClassConecta.conectajTorre());
                JasperViewer jrviewer = new JasperViewer(jasperPrint, false);
                jrviewer.setVisible(true);
        } catch (SQLException | JRException | IOException ex) {
            Logger.getLogger(jFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao gerar relatório\n"+ex);
        }

    }//GEN-LAST:event_visualizaLigacoesjButtonActionPerformed

