    private void ativaUsuariojButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ativaUsuariojButtonActionPerformed
        // TODO add your handling code here:
        
        
//         ************************ pessoas*************************
        
        // verifica se existe a pessoa cadastrada no programa (se estiver copiar codigo e jogar em usuarios)
        PESSOAS oPessoa = new PESSOAS();
        
        ResultSet rs2 = null;
        oPessoa.setLogin(UsuarioLogado);
        try {
            rs2 = oPessoa.getConsultar(conecta);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na consulta de pessoa!\n"+e, "ATEN√á√ÉO", JOptionPane.ERROR_MESSAGE);
        }
        try {
            while (rs2.next()){
                verifica_pessoa = rs2.getString("NOME_PESSOA");
                verifica_codigo_pessoa = rs2.getInt("CODIGO_PESSOA");
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher o nome da pessoa!\n"+e, "ATEN√á√ÉO", JOptionPane.ERROR_MESSAGE);
        }
        
        
//         ************************ usu√°rios*************************        
        
        
        // pega o codigo da pessoa e verifica se existe usuario cadastrado e pega o n√≠vel do usuario caso exista.
        Usuarios oUsuarios = new Usuarios();
        
        ResultSet rs = null;
        oUsuarios.setLogin(UsuarioLogado);
        try {
            oUsuarios.setCodigo_pessoa_usuario(verifica_codigo_pessoa);
            rs = oUsuarios.getConsultar(conecta);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na consulta de pessoa!\n"+e, "ATEN√á√ÉO", JOptionPane.ERROR_MESSAGE);
        }
        try {
            while (rs.next()){
                verifica_usuario = rs.getString("LOGIN");
                verifica_codigo_usuario = rs.getInt("CODIGO_PESSOA_USUARIO");
                nivel_de_acesso = rs.getString("NIVEL_USUARIO");
//                System.out.println(nivel_de_acesso);
//                System.out.println("verifica o codigo do usuario no bot√£o: "+verifica_codigo_usuario);
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher o nome da pessoa!\n"+e, "ATEN√á√ÉO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_ativaUsuariojButtonActionPerformed

