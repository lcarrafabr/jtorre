    private void relatorioCredenciadojButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_relatorioCredenciadojButtonActionPerformed
        // TODO add your handling code here:
        try {

            String caminhoRelJasper = "/Relatorios/RCredenciados.jasper";
            
                Map parametros = new HashMap();
                parametros.put("CODIGO_REGIONAL", recebe_codigo_regional);
                URL arquivo = getClass().getResource(caminhoRelJasper);
                if (arquivo == null) {
                    JOptionPane.showMessageDialog(null, "Arquivo jasper não encontrado");
                }
                JasperReport report;
                report = JasperManager.loadReport(arquivo.openStream());
                JasperPrint jasperPrint = JasperFillManager.fillReport(report, parametros, ClassConecta.getConectaContatosTorreDB());
                JasperViewer jrviewer = new JasperViewer(jasperPrint, false);
                jrviewer.setVisible(true);
        } catch (SQLException | JRException | IOException ex) {
            Logger.getLogger(jFPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_relatorioCredenciadojButtonActionPerformed

