    public void getGradeTipoTelefone() throws ParseException{
       //Consultar Pessoas
        TiposTelefones oTipoTelefone = new TiposTelefones();

        if ( tipoTelefonejTextField.getText().length() > 0 ){
            oTipoTelefone.setTipo_telefone(tipoTelefonejTextField.getText() +"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "Tipo telefone"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradetipoTelefonejTable.setModel(modeloTabela);
        gradetipoTelefonejTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradetipoTelefonejTable.getColumnModel().getColumn(1).setPreferredWidth(200);

try {
            resultSet = oTipoTelefone.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_TIPO_TELEFONE")),
                    resultSet.getString("TIPO_TELEFONE")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(TiposTelefones.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    public void getFieldTipoTelefone(int codigo_tipo_telefone) throws ParseException{
        //Buscar contato
        TiposTelefones oTiposTelefone = new TiposTelefones();
        ResultSet rs = null;
        oTiposTelefone.setCodigo_tipo_telefone(codigo_tipo_telefone);

        try {
            rs = oTiposTelefone.getConsultar(conecta);
            rs.next();

            //Pegar campos
            tipoTelefonejTextField.setText(rs.getString("TIPO_TELEFONE"));
            
        } catch (SQLException ex) {
            Logger.getLogger(TiposTelefones.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
