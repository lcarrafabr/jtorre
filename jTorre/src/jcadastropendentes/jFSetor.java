    public void getGradeSetor() throws ParseException{
       //Consultar Pessoas
        Setores oSetor = new Setores();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "Setor"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeSetorjTable.setModel(modeloTabela);
        gradeSetorjTable.getColumnModel().getColumn(0).setPreferredWidth(30);
        gradeSetorjTable.getColumnModel().getColumn(1).setPreferredWidth(300);

try {
            resultSet = oSetor.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_SETOR")),
                    resultSet.getString("NOME_SETOR")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Telefones.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    public void getFieldSetor(int codigo_setor) throws ParseException{
        //Buscar contato
        Setores oSetor = new Setores();
        ResultSet rs = null;
        oSetor.setCodigo_setor(codigo_setor);

        try {
            rs = oSetor.getConsultar(conecta);
            rs.next();

            //Pegar campos
            setorjTextField.setText(rs.getString("NOME_SETOR"));
            observacaojTextArea.setText(rs.getString("OBSERVACAO"));
            
        } catch (SQLException ex) {
            Logger.getLogger(Setores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
