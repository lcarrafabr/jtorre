    public void getGradePessoas() throws ParseException{
       //Consultar Pessoas
        Funcionarios oFuncionarios = new Funcionarios();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "Funcionário", "Matrícula", "Login", "Id Login"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradePessoaFuncionariojTable.setModel(modeloTabela);
        gradePessoaFuncionariojTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradePessoaFuncionariojTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        gradePessoaFuncionariojTable.getColumnModel().getColumn(2).setPreferredWidth(50);
        gradePessoaFuncionariojTable.getColumnModel().getColumn(3).setPreferredWidth(50);
        gradePessoaFuncionariojTable.getColumnModel().getColumn(3).setPreferredWidth(50);

try {
            oFuncionarios.setCodigo_pessoa_funcionario(recebe_codigo_pessoa);
            resultSet = oFuncionarios.getConsultar(conecta);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("CODIGO_PESSOA_FUNCIONARIO")),
                    resultSet.getString("NOME_PESSOA"),
                    resultSet.getString("MATRICULA"),
                    resultSet.getString("LOGIN"),
                    resultSet.getString("ID_LOGIN")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(CadastroPendentes.class.getName()).log(Level.SEVERE, null, ex);
        }

           }
    
    
    
    public void getFieldPessoa(int codigo_click_funcionario) throws ParseException{
        //Buscar contato
        Funcionarios oFuncionarios = new Funcionarios();
        ResultSet rs = null;
        oFuncionarios.setCodigo_pessoa_funcionario(codigo_click_funcionario);

        try {
            rs = oFuncionarios.getConsultar(conecta);
            rs.next();

            //Pegar campos
            matriculajTextField.setText(rs.getString("MATRICULA"));
            loginRedejTextField.setText(rs.getString("LOGIN"));
            idLoginjTextField.setText(rs.getString("ID_LOGIN"));
            ccmsjTextField.setText(rs.getString("CCMS"));
            dataAdmissaojFormattedTextField.setText(rs.getString("DATA_ADMISSAO"));
            dataUltimaFeriasjFormattedTextField.setText(rs.getString("DATA_ULTIMA_FERIAS"));
            dataDemissaojFormattedTextField.setText(rs.getString("DATA_DEMISSAO"));
            observacaojTextArea.setText(rs.getString("OBSERVACAO"));
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
    
