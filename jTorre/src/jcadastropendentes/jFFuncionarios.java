    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        Funcionarios oFuncionarios = new Funcionarios();
        
        if (agentejTextField.getText().equals("") || matriculajTextField.getText().equals("") || 
            loginRedejTextField.getText().equals("") || ccmsjTextField.getText().equals("") || 
            dataAdmissaojFormattedTextField.getText().equals("  /  /    ") ||
            dataUltimaFeriasjFormattedTextField.getText().equals("  /  /    ")){
            
            JOptionPane.showMessageDialog(null, "Atenção, preencha todos os campos", "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            
        }else{
            
            try {
                
                salvarjButton.setEnabled(false);
                novojButton.setEnabled(true);
                editarjButton.setEnabled(true);
                excluirjButton.setEnabled(false);
                
                oFuncionarios.setCodigo_pessoa_funcionario(recebe_codigo_pessoa);
                
                oFuncionarios.setMatricula(Integer.parseInt(matriculajTextField.getText().trim()));
                oFuncionarios.setLogin(loginRedejTextField.getText().trim().toUpperCase());
                oFuncionarios.setId_login(Integer.parseInt(idLoginjTextField.getText().trim()));
                oFuncionarios.setCcms(ccmsjTextField.getText().trim());
                oFuncionarios.setData_admissao(dataAdmissaojFormattedTextField.getText().toString());
                oFuncionarios.setData_ultima_ferias(dataUltimaFeriasjFormattedTextField.getText().toString());
                oFuncionarios.setData_demissao(dataDemissaojFormattedTextField.getText().toString());
                oFuncionarios.setObservacao(observacaojTextArea.getText());
                oFuncionarios.setCadastrar();               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar.\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
                
            }
            try {
            getGradePessoas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar grade.\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
            
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

