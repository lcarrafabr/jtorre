    public void getFieldContrLigacoesRecep(int codigo_controle_ligacoes) throws ParseException{
        //Buscar contato
        ControleLigacoesReceptivo oControleLigacoesReceptivo = new ControleLigacoesReceptivo();
        ResultSet rs = null;
        oControleLigacoesReceptivo.setCodigo_controle_ligacoes_rec(codigo_controle_ligacoes);

        try {
            rs = oControleLigacoesReceptivo.getConsultar(conecta);
            rs.next();

            //Pegar campos
            solicitantejComboBox.setSelectedItem(rs.getString("SOLICITANTE"));
            horaIniciojFormattedTextField.setText(rs.getString("HORA_INICIO"));
            horaFimjFormattedTextField.setText(rs.getString("HORA_FIM"));
            credenciadojTextField.setText(rs.getString("CREDENCIADO"));
            regionaljComboBox.setSelectedItem(rs.getString("REGIONAL"));
            motivojComboBox.setSelectedItem(rs.getString("MOTIVO"));
            conseguiuResolverjComboBox.setSelectedItem(rs.getString("CONSEGUIU_RESOLVER"));
            transferiujComboBox.setSelectedItem(rs.getString("TRANSFERIU"));
//            dataCadastrojFormattedTextField.setValue(Funcoes.getDataFormat(rs.getDate("DATA")));
            dataCadastrojFormattedTextField.setText(rs.getString("DATA"));
            
        } catch (SQLException ex) {
            Logger.getLogger(EnderecosEletronicos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao preencher os campos\n"+ex, "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    
    
